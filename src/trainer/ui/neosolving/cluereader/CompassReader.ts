export class CompassReader {
  constructor(public readonly capture: CapturedCompass, private calibration_functions: (_: AntialiasingType) => CompassCalibrationFunction = typ => CompassReader.calibration_tables[typ]) {
  }

  private _angle: Lazy<CompassReader.AngleResult> = lazy((): CompassReader.AngleResult => {
    const CENTER_SIZE = 2
    const OFFSET = CENTER_SIZE - 1
    const CENTER_OFFSET = {x: CapturedCompass.TOTAL_COMPASS_RADIUS, y: CapturedCompass.TOTAL_COMPASS_RADIUS}

    const buf = this.capture.compass_area.getData()

    const buf_center = {
      x: buf.width / 2,
      y: buf.height / 2
    }

    function getRed(x: number, y: number) {
      const i = 4 * ((CENTER_OFFSET.y + y) * buf.width + x + CENTER_OFFSET.x)

      return buf.data[i]
    }

    function isArrow(x: number, y: number) {
      const [r, g, b, _] = buf.getPixel(x, y)

      return r < 5 && g < 5 && b < 5
    }

    if (CompassReader.DEBUG_COMPASS_READER) {
      CompassReader.debug_overlay.clear()

      this.capture.compass_area.debugOverlay(CompassReader.debug_overlay)
    }

    const TARGET_X_SAMPLE_OFFSETS: Vector2[] = [
      {x: -15, y: -15},
      {x: -14, y: -14},
      {x: -13, y: -13},
      {x: -12, y: -12},
      {x: 12, y: 12},
      {x: 11, y: 11},
      {x: 10, y: 10},
      {x: 9, y: 9},
      {x: 13, y: -16},
      {x: 12, y: -15},
      {x: 11, y: -14},
      {x: 10, y: -13},
      {x: -15, y: 10},
      {x: -14, y: 9},
      {x: -13, y: 8},
      {x: -12, y: 7},
    ]

    if (TARGET_X_SAMPLE_OFFSETS.every(coords => {
      const red = getRed(coords.x, coords.y)

      return red >= 70 && red <= 110
    })) return {type: "likely_solved", pixel_count: undefined}

    const rectangle_samples: Vector2[] = []

    let antialiasing_detected = false

    const ANTIALIASING_SEARCH_RADIUS = 40

    for (let y = 0; y < buf.height; y++) {
      for (let x = 0; x < buf.width; x++) {
        if (isArrow(x, y)) {
          rectangle_samples.push({x, y})
        } else if (!antialiasing_detected && Vector2.max_axis(Vector2.sub({x, y}, buf_center)) <= ANTIALIASING_SEARCH_RADIUS) {
          const red = buf.getPixel(x, y)[0]

          if (red < 40) antialiasing_detected = true
        }
      }
    }

    if (antialiasing_detected) {
      if (rectangle_samples.length < 400) return {type: "likely_closed", pixel_count: rectangle_samples.length, details: `Not enough pixels (${rectangle_samples.length}) sampled for the rectangle sample. [MSAA]`}
      if (rectangle_samples.length < 1700) return {type: "likely_concealed", pixel_count: rectangle_samples.length, details: `Not enough pixels (${rectangle_samples.length}) sampled for the rectangle sample. [MSAA]`}
      if (rectangle_samples.length > 2250) return {type: "likely_concealed", pixel_count: rectangle_samples.length, details: `Too many pixels (${rectangle_samples.length}) sampled for the rectangle sample. [MSAA]`}
    } else {
      if (rectangle_samples.length < 400) return {type: "likely_closed", pixel_count: rectangle_samples.length, details: `Not enough pixels (${rectangle_samples.length}) sampled for the rectangle sample.`}
      if (rectangle_samples.length < 1900) return {type: "likely_concealed", pixel_count: rectangle_samples.length, details: `Not enough pixels (${rectangle_samples.length}) sampled for the rectangle sample.`}
      if (rectangle_samples.length > 2300) return {type: "likely_concealed", pixel_count: rectangle_samples.length, details: `Too many pixels (${rectangle_samples.length}) sampled for the rectangle sample.`}
    }

    const center_of_mass: Vector2 = {
      x: lodash.sumBy(rectangle_samples, v => v.x) / rectangle_samples.length,
      y: lodash.sumBy(rectangle_samples, v => v.y) / rectangle_samples.length
    }

    const center_of_area: Vector2 = Rectangle.center(Rectangle.from(...rectangle_samples), false)

    const angleof = (v: Vector2): number => {
      return Vector2.angle(ANGLE_REFERENCE_VECTOR, Vector2.normalize({x: v.x, y: -v.y}))
    }

    const initial_angle = angleof(Vector2.sub(center_of_mass, center_of_area))

    const angle_samples: {
      angle: number,
      weight: number
    }[] = rectangle_samples.flatMap(v => {

      const vector = Vector2.sub(v, center_of_area)

      let angle = angleof(vector)

      if (Number.isNaN(angle)) return []

      if (Angles.angleDifference(angle, initial_angle) > Math.PI / 2)
        angle = Angles.normalizeAngle(angle - Math.PI)

      return {
        angle: angle,
        weight: Vector2.lengthSquared(vector)
      }
    })

    const angle_after_rectangle_sample = Angles.normalizeAngle(Math.atan2(
      lodash.sum(angle_samples.map(a => a.weight * Math.sin(a.angle))),
      lodash.sum(angle_samples.map(a => a.weight * Math.cos(a.angle))),
    ))

    const calibration_mode: CompassReader.AntialiasingType = antialiasing_detected ? "msaa" : "off"

    const calibration_function = CompassReader.DISABLE_CALIBRATION ? CompassCalibrationFunction.none : this.calibration_functions?.(calibration_mode) ?? CompassCalibrationFunction.none

    const final_angle = calibration_function.apply(angle_after_rectangle_sample)

    if (CompassReader.DEBUG_COMPASS_READER) {
      log().log(`Angle: ${Angles.radiansToDegrees(initial_angle).toFixed(3)}° (CoM), ${Angles.radiansToDegrees(angle_after_rectangle_sample).toFixed(3)}° (rect), ${Angles.radiansToDegrees(final_angle.median).toFixed(3)}° (final)`,
        "Compass Reader"
      )

      CompassReader.debug_overlay.render()
    }

    return {
      type: "success",
      pixel_count: rectangle_samples.length,
      angle: final_angle,
      raw_angle: angle_after_rectangle_sample,
      antialiasing: antialiasing_detected
    }
  })

  getAngle(): CompassReader.AngleResult {
    return this._angle.get()
  }
}

